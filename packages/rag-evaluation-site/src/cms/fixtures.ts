export function formatCmsAssetSize(size: number): string {
	if (size >= 1_048_576) return `${(size / 1_048_576).toFixed(1)} MB`;
	if (size >= 1024) return `${Math.round(size / 1024)} KB`;
	return `${size} B`;
}

export function cmsAssetMeta(asset: CmsAsset): string {
	const format = (asset.mime.split("/")[1] ?? asset.mime).toUpperCase().replace("+XML", "");
	return `${format} · ${formatCmsAssetSize(asset.size)}`;
}
