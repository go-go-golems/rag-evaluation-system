#!/usr/bin/env python3
"""Generate a reviewable inventory of legacy helpers, v3 exports, and Widget adapters."""
from __future__ import annotations

import re
from pathlib import Path

ROOT = Path(__file__).resolve().parents[6]
OUT = Path(__file__).resolve().parents[1] / "sources" / "01-generated-runtime-inventory.md"


def block(text: str, marker: str) -> str:
    start = text.index(marker)
    brace = text.index("{", start)
    depth = 0
    for i in range(brace, len(text)):
        if text[i] == "{":
            depth += 1
        elif text[i] == "}":
            depth -= 1
            if depth == 0:
                return text[brace + 1 : i]
    raise ValueError(marker)


def quoted_map(text: str, marker: str) -> list[tuple[str, str]]:
    body = block(text, marker)
    return re.findall(r'"([A-Za-z0-9_]+)"\s*:\s*"([A-Za-z0-9_]+)"', body)


def exports(text: str, function_name: str, object_name: str) -> list[str]:
    body = block(text, f"func (r *runtime) {function_name}")
    return re.findall(rf'setExport\({re.escape(object_name)},\s*"([A-Za-z0-9_]+)"', body)


module = (ROOT / "pkg/widgetdsl/module.go").read_text()
v3 = (ROOT / "pkg/widgetdsl/v3.go").read_text()
crm = (ROOT / "pkg/widgetdsl/v3_crm.go").read_text()
registry = (ROOT / "packages/rag-evaluation-site/src/widgets/defaultRegistry.ts").read_text()

legacy = {
    "ui.dsl": quoted_map(module, "var uiHelpers"),
    "data.dsl": quoted_map(module, "var dataHelpers"),
    "context_window.dsl": quoted_map(module, "var contextWindowHelpers"),
    "course.dsl": quoted_map(module, "var courseHelpers"),
    "cms.dsl": quoted_map(module, "var cmsHelpers"),
}

v3_exports = {
    "ui": exports(v3, "v3UIObject", "ui"),
    "data": exports(v3, "v3DataObject", "data"),
    "cms": exports(v3, "v3CMSObject", "cms"),
    "course": exports(v3, "v3CourseObject", "course"),
    "context": exports(v3, "v3ContextObject", "context"),
    "schedule": exports(v3, "v3ScheduleObject", "schedule"),
    "crm": exports(crm, "v3CRMObject", "crm"),
}

# Time is installed in a slightly different function shape but uses the same pattern.
v3_exports["time"] = exports(v3, "v3TimeObject", "timeObj")

registry_groups: dict[str, list[str]] = {}
for group in ["ui", "data", "time", "contextWindow", "course", "cms"]:
    match = re.search(
        rf"export const {group}WidgetRegistry = createWidgetRegistry\(\[(.*?)\]\);",
        registry,
        re.S,
    )
    if match:
        registry_groups[group] = re.findall(r"\b([A-Za-z0-9_]+Widget)\b", match.group(1))

lines = [
    "# Generated Widget DSL Runtime Inventory",
    "",
    "Generated by `scripts/01-inventory-widget-dsl.py`. This is evidence, not the final API recommendation.",
    "",
    "## Legacy helper maps",
    "",
]
for name, items in legacy.items():
    lines.extend([f"### `{name}` — {len(items)} component helpers", ""])
    lines.append(", ".join(f"`{helper}` → `{component}`" for helper, component in items))
    lines.append("")

lines.extend(["## Widget DSL v3 top-level namespace exports", ""])
for name, items in v3_exports.items():
    lines.extend([f"### `widget.{name}` — {len(items)} exports", "", ", ".join(f"`{item}`" for item in items), ""])

lines.extend(["## Default React Widget registry groups", ""])
for name, items in registry_groups.items():
    lines.extend([f"### `{name}WidgetRegistry` — {len(items)} adapters", "", ", ".join(f"`{item}`" for item in items), ""])

OUT.write_text("\n".join(lines) + "\n")
print(OUT)
